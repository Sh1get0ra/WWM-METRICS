/* 玉ねぎ楷書「激」→ 静的見出し文字列の SVG path 生成 (2026-06-12)
 *
 * 規約整理: フォントの webfont 配信 (woff2) = 「フォントとして再利用できる形」でグレー →
 * 「一部の文字の画像化 = 自由」(明示許可) に収める。配信されるのは使用文字の輪郭 path のみ。
 *
 * 入力: tools/tamanegi-geki.ttf (gitignore — 実行はフォント所持ローカルのみ)
 *       data/i18n/ui.json (対象 key の ja 値が真実源)
 * 出力: assets/kaisho-paths.js (window.WWM_KAISHO = { key: {vb, d, text} })
 *       → workspace.js の WWMKaisho.apply() が inline <svg><path> を組む
 * 実行: node scripts/build-kaisho-svg.cjs
 * 文字列/対象 key 変更時は再実行 + kaisho-paths.js の ?v bump
 */
const fs = require('fs');
const path = require('path');
const opentype = require('opentype.js');

const ROOT = path.join(__dirname, '..');
const FONT_PATH = path.join(ROOT, 'tools', 'tamanegi-geki.ttf');
const UI_JSON = path.join(ROOT, 'data', 'i18n', 'ui.json');
const OUT = path.join(ROOT, 'assets', 'kaisho-paths.js');
const SIZE = 64; // 生成基準 (ベクタ = 表示サイズは CSS)

// 対象: ui.json key (ja 値を引く) + literal (i18n 外の静的文字)
const UI_KEYS = [
  'wsTabBuild', 'wsTabAnlz', 'wsTabEnbu', 'wsTabHist', // tab (PC + mobile bottom nav)
  'wsTabGear', 'wsTabXinfa',                            // 扁額題字 (武備 panel 区画)
  'heroSeatScore', 'heroSeatDps',                       // hero 題字
  'railStatus',                                         // rail 頭
  'brandVert',                                          // topbar 縦書きブランド「風燕計」
  'martialHistoryTab',                                  // 武格履歴 panel 題字 (2026-06-15 追加)
  // modal title 8 件 (2026-06-15 追加 — フォント整理一気 sprint)
  'shareTitle',         // 飛簡
  'cmpTitleJa',         // 武具対照
  'xinfaTitleJa',       // 心法対照
  'cmpArsenalTitle',    // 武庫対照
  'importSetupTitle',   // 引燕
  'importPreviewTitle', // 引燕 (= importSetupTitle と同字、 重複生成は OK)
  'noteTitleJa',        // 筆記
  'diagTitleJa',        // 弱点指摘
  'ocrHelpTitle',       // 画面取込指南 (2026-06-15 ja 漢字化、 旧「スクショ取込ガイド」)
  'qishuPickerTitle',   // 奇術選択
];
const LITERALS = {
  qishu: '奇術', // index.html 直書き (data-i18n なし)
  // 縦書き brand「風燕計」 1 文字 entry — svg inline-block は writing-mode で 90deg 倒れる
  // → 文字毎に svg 生成 + DOM で縦積み (kaishoApply の brandVert 分岐で組立)
  brandV1: '風',
  brandV2: '燕',
  brandV3: '計',
  railKanji: '斬', // rail 頭 ステータス panel 透かし (.kanji-mark)
  heroKanji: '侠', // hero panel 右端 透かし (.hero-wuxia .kanji-mark)
  donutDmgCenter: '勁率', // luopan 中心 label (2026-06-22 兄貴指示で玉ねぎ化、 ttf 字形あり)
};

if (!fs.existsSync(FONT_PATH)) {
  console.error('font not found: ' + FONT_PATH + ' (gitignore 対象 — フォント所持ローカルで実行)');
  process.exit(1);
}
const buf = fs.readFileSync(FONT_PATH);
const font = opentype.parse(buf.buffer.slice(buf.byteOffset, buf.byteOffset + buf.byteLength));
const ui = JSON.parse(fs.readFileSync(UI_JSON, 'utf8'));

const targets = {};
for (const k of UI_KEYS) {
  if (!ui[k] || !ui[k].ja) { console.warn('SKIP (ui.json に無い): ' + k); continue; }
  targets[k] = ui[k].ja;
}
Object.assign(targets, LITERALS);

const out = {};
for (const [key, text] of Object.entries(targets)) {
  // グリフ存在チェック (.notdef = index 0)
  const missing = [...text].filter((ch) => font.charToGlyphIndex(ch) === 0);
  if (missing.length) {
    console.warn(`SKIP ${key} "${text}" — グリフ無し: ${missing.join(' ')}`);
    continue;
  }
  const p = font.getPath(text, 0, 0, SIZE, { kerning: true });
  const bb = p.getBoundingBox();
  const pad = 2;
  const vb = [bb.x1 - pad, bb.y1 - pad, bb.x2 - bb.x1 + pad * 2, bb.y2 - bb.y1 + pad * 2]
    .map((n) => +n.toFixed(1)).join(' ');
  out[key] = { vb, d: p.toPathData(2), text };
  console.log(`OK ${key} "${text}" (${(p.toPathData(2).length / 1024).toFixed(1)}KB)`);
}

const banner =
  '// GENERATED by scripts/build-kaisho-svg.cjs — 手編集禁止 (再生成で消える)\n' +
  '// 字形出典: 玉ねぎ楷書「激」無料版 (玉点堂) — 使用文字のみの画像化 (規約明示許可範囲)。\n' +
  '// フォントファイル本体は配布していない。\n';
fs.writeFileSync(OUT, banner + 'window.WWM_KAISHO = ' + JSON.stringify(out) + ';\n');
const kb = (fs.statSync(OUT).size / 1024).toFixed(1);
console.log(`\nwrote ${OUT} (${kb}KB, ${Object.keys(out).length} entries)`);
